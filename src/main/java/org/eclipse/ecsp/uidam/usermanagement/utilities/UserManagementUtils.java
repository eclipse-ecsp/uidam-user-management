package org.eclipse.ecsp.uidam.usermanagement.utilities;

import java.util.Map;
import java.util.concurrent.ConcurrentHashMap;
import java.util.function.Function;
import java.util.function.Predicate;

/**
 * Utility class for UserManagement.
 */
public final class UserManagementUtils {

    private UserManagementUtils() {

    }

    /**
     * Method to get distinct Object based on certain criteria.
     *
     * @param keyExtractor keyExtractor
     * @param <T> Object can be of any type.
     * @return distinct values.
     */
    public static <T> Predicate<T> distinctByKey(Function<? super T, Object> keyExtractor) {
        Map<Object, Boolean> map = new ConcurrentHashMap<>();
        return t -> map.putIfAbsent(keyExtractor.apply(t), Boolean.TRUE) == null;
    }

}
